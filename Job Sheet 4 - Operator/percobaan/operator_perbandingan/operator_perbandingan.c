#include <stdio.h>

void main(){
	int a = 5;
	int b = 8;
	
	printf("a = %d\n",a);
	printf("b = %d\n",b);
	printf("------\n");
	
	printf("a > b = %d\n",a > b);
	printf("a < b = %d\n",a < b);
	printf("a >= b = %d\n",a >= b);
	printf("a <= b = %d\n",a <= b);
	printf("a == b = %d\n",a == b);
	printf("a != b = %d\n",a != b);
}
