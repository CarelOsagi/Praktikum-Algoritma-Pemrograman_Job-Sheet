#include <stdio.h>

void main(){
	int a = 4;
	
	printf("a = %d\n",a);
	printf("------\n");
	
	//increment a
	a++;
	printf("a++ = %d\n",a);
	
	++a;
	printf("++a = %d\n",a);
	
	//decrement a
	a--;
	printf("a-- = %d\n",a);
	
	--a;
	printf("--a = %d\n",a);
}
